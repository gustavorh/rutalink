type IconType =
  | "truck"
  | "vehicle"
  | "driver"
  | "clock"
  | "clients"
  | "suppliers"
  | "routes"
  | "report";

export const getIcon = (type: IconType, className?: string) => {
  const icons = {
    truck: <TruckIcon className={className} />,
    vehicle: <VehicleIcon className={className} />,
    driver: <DriverIcon className={className} />,
    clock: <ClockIcon className={className} />,
    clients: <ClientIcon className={className} />,
    suppliers: <SupplierIcon className={className} />,
    routes: <RouteIcon className={className} />,
    report: <ReportIcon className={className} />,
  };

  return icons[type];
};
